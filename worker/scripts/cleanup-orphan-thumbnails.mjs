// One-shot cleanup: scan inventory_thumbnails for rows whose image_url
// points to a /api/media/view/<file> URL where the underlying KV bytes
// are missing. Probes the live worker (which now serves an SVG
// placeholder + X-Placeholder header for missing media) and emits SQL
// DELETE statements for the orphan rows.
//
// Usage:
//   node scripts/cleanup-orphan-thumbnails.mjs > cleanup.sql
//   npx wrangler d1 execute shinel-db --remote --file=cleanup.sql
//
// Idempotent — re-running is safe; the second pass will find zero
// orphans (because they're deleted).
const WORKER = "https://shinel-auth.shinelstudioofficial.workers.dev";

async function main() {
  const res = await fetch(`${WORKER}/thumbnails`);
  if (!res.ok) throw new Error(`/thumbnails HTTP ${res.status}`);
  const data = await res.json();
  const rows = Array.isArray(data?.thumbnails) ? data.thumbnails : [];

  const candidates = rows.filter(
    (r) => typeof r.imageUrl === "string" && r.imageUrl.startsWith("/api/media/view/")
  );

  console.error(`-- Probing ${candidates.length} candidate thumbnail rows…`);
  const orphanIds = [];

  // Probe with GET (HEAD doesn't match the worker's GET-only handler,
  // so HEAD silently falls through to the 404 catch-all). The
  // X-Placeholder header tells us the bytes are missing.
  for (const row of candidates) {
    const probeUrl = `${WORKER}${row.imageUrl}`;
    try {
      const resp = await fetch(probeUrl);
      const placeholder = resp.headers.get("x-placeholder");
      if (placeholder === "missing-media") {
        orphanIds.push({ id: row.id, imageUrl: row.imageUrl, filename: row.filename });
      }
    } catch (e) {
      console.error(`-- probe failed for ${row.id}: ${e.message}`);
    }
  }

  console.error(`-- Found ${orphanIds.length} orphan rows.`);
  if (orphanIds.length === 0) {
    console.log("-- No orphans to delete.");
    return;
  }

  console.log("-- Generated by cleanup-orphan-thumbnails.mjs");
  console.log(`-- Total orphans: ${orphanIds.length}`);
  for (const o of orphanIds) {
    console.log(`-- ${o.id}  (${o.filename})  ${o.imageUrl}`);
  }
  console.log("");
  for (const o of orphanIds) {
    // Cascade through media_collection_items first so we don't leave
    // dangling collection memberships.
    console.log(`DELETE FROM media_collection_items WHERE media_id = '${o.id.replace(/'/g, "''")}' AND media_type = 'thumbnail';`);
    console.log(`DELETE FROM inventory_thumbnails WHERE id = '${o.id.replace(/'/g, "''")}';`);
  }
}

main().catch((e) => {
  console.error(`error: ${e.message}`);
  process.exit(1);
});
